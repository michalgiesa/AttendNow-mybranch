    <h1>Regulamin platformy AttendNow</h1>

    <h5>§1 Postanowienia ogólne</h5>
    <p>1. Niniejszy regulamin określa zasady korzystania z platformy internetowej AttendNow (dalej zwaną platformą), umożliwiającej tworzenie i udostępnianie eventów oraz spotkań 
        kulturalnych.<br>
    2. Korzystanie z platformy jest dobrowolne i równoznaczne z akceptacją niniejszego regulaminu.<br>
    3. Wszelkie prawa do strony internetowej oraz treści na niej zawartych, w tym prawo autorskie, są własnością AttendNow.<br>
    4. Platforma jest prowadzona przez firmę AttendNow sp. z.o.o., z siedzibą w Opolu, ul. Prószkowska 76, 45-758, zarejestrowaną w rejestrze handlowym.</p>

    <h5>§2 Definicje</h5>
    <p>1. Użytkownik - osoba korzystająca z platformy.<br>
    2. Organizator - osoba lub firma tworząca i publikująca wydarzenie lub spotkanie kulturalne na platformie.<br>
    3. Uczestnik - osoba biorąca udział w wydarzeniu lub spotkaniu kulturalnym organizowanym przez Organizatora.<br>
    4. Wydarzenie lub spotkanie kulturalne - impreza lub spotkanie o charakterze kulturalnym, organizowane przez Organizatora.</p>

    <h5>§3 Rejestracja i konto użytkownika</h5>
    <p>1. Korzystanie z niektórych funkcjonalności platformy wymaga rejestracji i utworzenia konta użytkownika.<br>
    2. Rejestracja jest możliwa dla osób pełnoletnich posiadających pełną zdolność do czynności prawnych.<br>
    3. W trakcie rejestracji Użytkownik zobowiązany jest podać prawdziwe dane osobowe.<br>
    4. Użytkownik ponosi pełną odpowiedzialność za bezpieczeństwo i poufność danych logowania.<br>
    5. Platforma zastrzega sobie prawo do blokowania konta użytkownika lub usunięcia konta w przypadku naruszenia przez Użytkownika postanowień regulaminu lub obowiązującego prawa.</p>

    <h5>§4 Tworzenie wydarzeń lub spotkań kulturalnych</h5>
    <p>1. Organizator może tworzyć i publikować wydarzenia lub spotkania kulturalne na platformie.<br>
    2. Publikacja wydarzenia lub spotkania kulturalnego wymaga podania nazwy, opisu, daty, miejsca oraz informacji o cenie i liczbie miejsc.<br>
    3. Organizator ponosi pełną odpowiedzialność za prawdziwość i aktualność informacji podanych w opisie wydarzenia lub spotkania kulturalnego.<br>
    4. Platforma zastrzega sobie prawo do moderacji opisów wydarzeń lub spotkań kulturalnych przed ich publikacją.</p>

    <h5>§5 Rezerwacje i płatności</h5>
    <p>1. Uczestnik może dokonać rezerwacji na wydarzenie lub spotkanie kulturalne poprzez platformę.<br>
    2. Organizator może ustawić cenę za udział w wydarzeniu lub spotkaniu kulturalnym.<br>
    3. Platformay może pobierać prowizję od Organizatora za korzystanie z Serwisu oraz opłatę manipulacyjną od Uczestnika za dokonanie rezerwacji poprzez platformę.<br>
    4. Płatności za udział w wydarzeniach lub spotkaniach kulturalnych dokonywane są bezpośrednio między Organizatorem a Uczestnikiem.<br>
    5. Platforma nie ponosi odpowiedzialności za jakość usług świadczonych przez Organizatora oraz jakość wydarzenia lub spotkania kulturalnego.</p>

    <h5>§6 Zasady korzystania z platformy</h5>
    <p>1. Korzystanie z platformy jest dozwolone tylko w celu organizacji i uczestnictwa w wydarzeniach lub spotkaniach kulturalnych.<br>
    2. Zabronione jest publikowanie treści o charakterze niezgodnym z prawem, naruszających dobra osobiste lub dobre obyczaje.<br>
    3. Zabronione jest publikowanie treści zawierających treści nieodpowiednie lub obraźliwe dla innych Użytkowników lub osób trzecich.<br>
    4. Zabronione jest dokonywanie działań mających na celu naruszenie bezpieczeństwa platformy lub jego użytkowników, w szczególności dostępu do danych nieuprawnionych.<br>
    5. Platforma zastrzega sobie prawo do blokowania dostępu do platformy lub usuwania treści naruszających regulamin.</p>

    <h5>§7 Odpowiedzialność</h5>
    <p>1. Użytkownik ponosi pełną odpowiedzialność za treści publikowane na platformie.<br>
    2. Organizator ponosi pełną odpowiedzialność za organizację wydarzenia lub spotkania kulturalnego oraz ich przebieg.<br>
    3. Platforma nie ponosi odpowiedzialności za treści publikowane przez Użytkowników oraz za organizację i przebieg wydarzeń lub spotkań kulturalnych.</p>

    <h5>§8 Postanowienia dotyczące ochrony danych osobowych</h5>
    <p>1. Administratorem danych osobowych Użytkowników i Organizatorów jest platforma.<br>
    2. Dane osobowe Użytkowników i Organizatorów są przetwarzane zgodnie z przepisami prawa, w tym z Rozporządzeniem Parlamentu Europejskiego i Rady (UE) 2016/679 z dnia 27 kwietnia 2016 r.
     w sprawie ochrony osób fizycznych w związku z przetwarzaniem danych osobowych i w sprawie swobodnego przepływu takich danych oraz uchylenia dyrektywy 95/46/WE (RODO).<br>
    3. Platforma zbiera i przetwarza dane osobowe Użytkowników i Organizatorów wyłącznie w celu realizacji usługi oraz w celach marketingowych, w przypadku wyrażenia na to zgody przez 
    Użytkowników lub Organizatorów.<br>
    4. Użytkownik i Organizator mają prawo dostępu do swoich danych osobowych, ich poprawiania, usunięcia lub ograniczenia przetwarzania.<br>
    5. Platforma zapewnia odpowiednie środki techniczne i organizacyjne zapewniające bezpieczeństwo przetwarzania danych osobowych Użytkowników i Organizatorów.</p>

    <h5>§9 Postanowienia dotyczące prawa właściwego</h5>
    <p>1. Regulamin oraz wszelkie kwestie związane z korzystaniem z platformy podlegają prawu polskiemu.<br>
    2. W sprawach spornych sądem właściwym będzie sąd powszechny właściwy dla siedziby platformy.</p>

    <h5>§10 Postanowienia końcowe</h5>
    <p>1. Platforma zastrzega sobie prawo do zmiany regulaminu w dowolnym czasie.<br>
    2. Zmiany regulaminu będą publikowane na stronie internetowej platformy.<br>
    3. W przypadku zmiany regulaminu Użytkownik będzie informowany o tym fakcie poprzez platformę lub na podany podczas rejestracji adres e-mail.<br>
    4. Regulamin obowiązuje od dnia opublikowania na stronie internetowej platformy.</p>

    <h5>§11 Kontakt</h5>
    <p>1. W przypadku jakichkolwiek pytań lub wątpliwości związanych z platformą, Użytkownik może skontaktować się z platformą za pośrednictwem formularza kontaktowego dostępnego na 
        stronie internetowej platformy lub za pośrednictwem adresu e-mail: kontakt@attendnow.com.</p>
